package gol.four.ldcc.gol.model;

public class Apply {
    int applyTpe, containerNo, employeeIdx, confirmIdx;
    String registerDate, confirmDate;
    int applyState;

    public int getApplyTpe() {
        return applyTpe;
    }

    public void setApplyTpe(int applyTpe) {
        this.applyTpe = applyTpe;
    }

    public int getContainerNo() {
        return containerNo;
    }

    public void setContainerNo(int containerNo) {
        this.containerNo = containerNo;
    }

    public int getEmployeeIdx() {
        return employeeIdx;
    }

    public void setEmployeeIdx(int employeeIdx) {
        this.employeeIdx = employeeIdx;
    }

    public int getConfirmIdx() {
        return confirmIdx;
    }

    public void setConfirmIdx(int confirmIdx) {
        this.confirmIdx = confirmIdx;
    }

    public String getRegisterDate() {
        return registerDate;
    }

    public void setRegisterDate(String registerDate) {
        this.registerDate = registerDate;
    }

    public String getConfirmDate() {
        return confirmDate;
    }

    public void setConfirmDate(String confirmDate) {
        this.confirmDate = confirmDate;
    }

    public int getApplyState() {
        return applyState;
    }

    public void setApplyState(int applyState) {
        this.applyState = applyState;
    }
}
